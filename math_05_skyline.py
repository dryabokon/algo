import numpy
# ----------------------------------------------------------------------------------------------------------------------
def skyline(G):

	res = 0
	max_a0 = numpy.max(G, axis=0)
	max_a1 = numpy.max(G, axis=1)
	for i in range(0,len(G)):
		for j in range(0, len(G[0])):
			res+= min(max_a0[j],max_a1[i])-G[i][j]

	return res
# ----------------------------------------------------------------------------------------------------------------------
if __name__ == '__main__':

	G = [[3,0,8,4],[2,4,5,7],[9,2,6,3],[0,3,1,0]]


	res = skyline(G)
	print(res)

	