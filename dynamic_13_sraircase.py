import numpy
# ----------------------------------------------------------------------------------------------------------------------
def count_number_of_ways(N,steps=[1,2]):

    D = [0]*(N+1)
    D[0]=1
    for i in range(0,N+1):
        if D[i]>0:
            for step in steps:
                if i+step<N+1:
                    D[i+step]+=D[i]


    return D[-1]
# ----------------------------------------------------------------------------------------------------------------------
if __name__ == '__main__':
    n_ways = count_number_of_ways(10)
    print(n_ways)
